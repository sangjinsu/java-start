package variable;

public class Var4 {
    public static void main(String[] args) {
        int a = 0, b = 1, c = 3, d = 5;
        System.out.println(a + " " + b + " " + c + " " + d);

        double e = 1.43;
        boolean f = false;
        char g = 'a';
        String s = "abcdefghijklmnopqrstuvwxyz";


        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(s);

    }
}  
