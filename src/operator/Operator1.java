package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        int sum = a + b;
        int diff = a - b;
        int multiple = a * b;
        int divide = a / b;
        int divisor = a % b;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(multiple);
        System.out.println(divide);
        System.out.println(divisor);
    }
}
